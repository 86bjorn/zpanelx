class runtime_haystack {

    /**
     * Get a value between two given strings.
     * @param string $string
     * @param type $start
     * @param type $end
     * @return type 
     */
    static function GetValueBetween($string, $start, $end) {
        $string = " " . $string;
        $ini = strpos($string, $start);
        if ($ini == 0)
            return "";
        $ini += strlen($start);
        $len = strpos($string, $end, $ini) - $ini;
        return substr($string, $ini, $len);
    }

}

